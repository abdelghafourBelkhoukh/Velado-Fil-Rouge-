<script>
import axios from 'axios';


export default{
    name: 'CheckoutPage',
    data(){
        return{
            UserData: {
                id : localStorage.getItem('id'),
                firstName: localStorage.getItem('firstName'),
                lastName: localStorage.getItem('lastName'),
                email: localStorage.getItem('email'),
                address: localStorage.getItem('address'),
                city: localStorage.getItem('city'),
                zip: localStorage.getItem('zipcode'),
                country: localStorage.getItem('country'),
            }
        }
    },
    computed: {
        cartData() {
            return this.$store.state.cartData;
        },
        totalPriceItems() {
            let total = 0;
            this.cartData.forEach((item) => {
                total += item.price * item.qty;
            });
            return total;
        },
    },
    mounted() {
    this.$store.dispatch("getCart");
    },
    methods: {
        addOrder(id) {
            this.cartData.forEach((item) => {
                let NewQuantity = 0;
                NewQuantity = item.quantity - item.qty;
                axios.put('http://localhost/fil-rouge/backend/Api/Stock/StockController.php', {
                    id: item.productID,
                    quantity: NewQuantity,
                })
            });
            axios.post('http://localhost/fil-rouge/backend/Api/order/OrderController.php', {userID: id,})
            .then(response => {
                console.log(response.data);
                this.$store.state.cartData = [];
                this.$router.push('/');
            })
            .catch(error => {
                console.log(error);
            });
        }
    }
}

</script>